package session3;

public class DoWhileExample {

    public static void main(String[] args) {

        int i=0;
        do{
            System.out.println("i = " + i);
            i++;
        }while (i<10);

        int k =0;

        do{
            System.out.println("k = " + k);
            k++;
        }while(k<-1);
    }
}
